
public class BooleanExample {

	public static void main(String[] args) {
		
		
		//boolean 타입을 사용해보자! : true/false (참/거짓) 값 만을 가질 수 있다.
		//그럼이 이 타입의 변수는 어디에 사용하는가? 주로 조건문/반복문 만들 때 사용
		boolean stop =false;
		
		if(stop) { //조건이 참일 때를 의미
			
			System.out.println("중지합니다.");
			
		}else { // 조건이 거짓일 때를 의미
			System.out.println("시작합니다.");
		
		} //if-else
	}//main

} //end class
